public class ApplyVO {

	private String applyIdx;
	private String recIdx;
	private String resIdx;
	private String memIdx;
	
	public String getApplyIdx() {
		return applyIdx;
	}
	public void setApplyIdx(String applyIdx) {
		this.applyIdx = applyIdx;
	}
	public String getRecIdx() {
		return recIdx;
	}
	public void setRecIdx(String recIdx) {
		this.recIdx = recIdx;
	}
	public String getResIdx() {
		return resIdx;
	}
	public void setResIdx(String resIdx) {
		this.resIdx = resIdx;
	}
	public String getMemIdx() {
		return memIdx;
	}
	public void setMemIdx(String memIdx) {
		this.memIdx = memIdx;
	}	
}
